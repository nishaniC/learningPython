class Mobile:
    def __init__(self,num):
        self.number = num
        
    def turn_on(self,):
        return 'mobile phone'+ self.number+' is turned on'
        
    def turn_off(self):
        return 'mobile phone is turned off'
        
    def call(self,num2):
        return 'calling ' + num2
        
        
m1=Mobile("01632-960004")
m2=Mobile("01632-960012")    

mlist=[m1,m2]
for x in mlist:
    print(x.turn_on())
    print(x.call('555-34343'))
    print(x.turn_off())
    
